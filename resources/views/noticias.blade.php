<h3>ASAE apreende cerca de 62 mil peças de “roupa de marca”</h3>
<p> A investigação materializou-se em intervenção sobre uma unidade industrial, no município de Vila do Conde, que, apesar de licenciada somente para armazenamento de artigos têxteis, também operava como unidade de confeção e de embalamento.

Apurou-se que, como modus operandi, os visados recebiam os artigos têxteis provenientes de fábricas nacionais, como calças, camisolas ou blusões, vindo a maioria em “cru” (sem qualquer marca aposta ou, consoante as peças, apenas com bordados exteriores), sendo colocadas nos artigos, naquelas instalações, as etiquetas de tecido de marcas protegidas falsificadas. Procediam, ainda, ao seu embalamento individual e colocavam já as respetivas etiquetas de preços de cartão exteriores, com um layout idêntico ao original.z</p>
